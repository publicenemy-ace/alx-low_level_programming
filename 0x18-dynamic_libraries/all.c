#include <stdio.h>

/**
 * add - add two integers
 * @a - first int
 * @b - second int
 * Return - addition
 */
int add (int a, int b)
{ 
	return (a + b);
}

/**
 * sub -subtract two integers
 * @a - first int
 * @b - second int
 * Return - subtract the final results
 */
int subtract (int a, int b)
{ 
	return (a - b);
}

/**
 * div - divide two integers
 * @a - first int
 * @b - second int
 * Return - divide
 */
int div (int a, int b)
{ 
	return (a / b);
}

/**
 * mod - mod of  two integers
 * @a - first int
 * @b - second int
 * Return - final results
 */
int mod (int a, int b)
{ 
	return (a % b);
}

/**
 * mul - multiply two integers
 * @a - first int
 * @b - second int
 * Return - multiply
 */
int mul(int a, int b)
{ 
	return (a * b);
}
